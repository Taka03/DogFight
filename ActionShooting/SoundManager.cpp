//=============================================================================
//コンストラクタ
//=============================================================================
CSoundManager::CSoundManager(void)
{
	m_mapSound.clear();
}

//=============================================================================
//デストラクタ
//=============================================================================
CSoundManager::~CSoundManager(void)
{
	m_mapSound.clear();
}

//=============================================================================
//サウンドリストへの追加
//=============================================================================
//[input]
//	pDataName:データ名
//	spSoundObj:追加するサウンドオブジェクト
//=============================================================================
void CSoundManager::AppendSoundList( const char *pDataName, sp<CSoundObject> spSoundObj )
{
	m_mapSound.insert( make_pair( pDataName, spSoundObj ) );
}

//=============================================================================
//音の再生
//=============================================================================
//[input]
//	pDataName:再生する音
//=============================================================================
void CSoundManager::Play( const char *pDataName )
{
	m_mapSound[pDataName]->Play();
} 

//=============================================================================
//音の停止
//=============================================================================
//[input]
//	pDataName:停止する音
//=============================================================================
void CSoundManager::Stop( const char *pDataName )
{
	m_mapSound[pDataName]->Stop();
}

