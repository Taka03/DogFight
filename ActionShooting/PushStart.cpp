//===========================================================================
CPushStart::CPushStart( const char *pName, const char *pDataName )
:CSprite( pName, pDataName ),
 m_Counter( 0 ),
 m_FlashCount( 30 )

{

	//パラメーターの読み込み
	LoadParameter( pDataName );

	Init();
	
	this->SetVisibleFlag( true );


}

//============================================================================
//デストラクタ
//============================================================================
CPushStart::~CPushStart(void)
{
}

//============================================================================
//レンダリング
//============================================================================
void CPushStart::Rendering()
{
	if( ++m_Counter < m_FlashCount )
	{
	
		m_pSpr->Rendering();
	}
	
	else if( m_Counter > m_FlashCount * 2 )
	{
		m_Counter = 0;
	}
	
}

